package vehicle;

public abstract class Vehicle implements VehicleInterface {
    

    private int weight;
    private String colour;

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public String getColour() {
        return colour;
    }

    public void setColour(String colour) {
        this.colour = colour;
    }
}
